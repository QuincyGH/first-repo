package org.qgh;

public class Main {
    public static void main(String[] args) {
        // 打印开始标记
        System.out.println("Hello and welcome!");
        // 循环打印数字1~5
        for (int i = 1; i <= 5; i++) {
            System.out.println("i = " + i);
        }
        // 打印完结标记
        System.out.println("Print end!");
    }
}